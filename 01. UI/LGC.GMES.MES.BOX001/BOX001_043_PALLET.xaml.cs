        private void PrintView()
        {
            try
            {
                cr = new C1.C1Report.C1Report();
                cr.Layout.PaperSize = System.Drawing.Printing.PaperKind.A5;

                System.Reflection.Assembly a = System.Reflection.Assembly.GetExecutingAssembly();

                using (Stream stream = a.GetManifestResourceStream("LGC.GMES.MES.BOX001.Report.Pallet_Shipping.xml"))
                {
                    cr.Load(stream, "Pallet_Shipping");

                    for (int col = 0; col < palletInfo.Columns.Count; col++)
                    {
                        string strColName = palletInfo.Columns[col].ColumnName;
                        if (cr.Fields.Contains(strColName))
                        {
                            cr.Fields[strColName].Text = palletInfo.Rows[0][strColName].ToString();
                            if (!showRemark)
                            {
                                cr.Fields["remark_Title"].Visible = false;
                                cr.Fields["remark_Bar"].Visible = false;
                                cr.Fields["CUSTMTRLID"].Visible = false;
                            }
                        }
                    }
                }

                c1DocumentViewer.Document = cr.FixedDocumentSequence;
                c1DocumentViewer.Zoom = 105;
                

            }
            catch (Exception ex)
            {
                throw ex;
            }
        }

